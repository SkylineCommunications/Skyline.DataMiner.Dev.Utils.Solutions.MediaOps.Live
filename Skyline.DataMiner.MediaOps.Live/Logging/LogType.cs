namespace Skyline.DataMiner.MediaOps.Live.Logging
{
	/// <summary>
	/// Defines the severity levels for logging.
	/// </summary>
	public enum LogType
	{
		/// <summary>
		/// Debug log level.
		/// </summary>
		Debug = 0,

		/// <summary>
		/// Information log level.
		/// </summary>
		Information = 1,

		/// <summary>
		/// Warning log level.
		/// </summary>
		Warning = 2,

		/// <summary>
		/// Error log level.
		/// </summary>
		Error = 3,
	}
}
