namespace Skyline.DataMiner.MediaOps.Live.Logging
{
	/// <summary>
	/// Defines the severity level of log messages.
	/// </summary>
	public enum LogType
	{
		/// <summary>
		/// Debug level logging.
		/// </summary>
		Debug = 0,

		/// <summary>
		/// Informational level logging.
		/// </summary>
		Information = 1,

		/// <summary>
		/// Warning level logging.
		/// </summary>
		Warning = 2,

		/// <summary>
		/// Error level logging.
		/// </summary>
		Error = 3,
	}
}
